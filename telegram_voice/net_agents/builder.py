"""BUILDER agent — grows the Hermes network by building new agents.

Given a spec ("build an agent called 'weather' that reports the forecast"),
the Builder creates a *complete, same-spec* agent:

  1. a dedicated Hermes brain (a `hermes` profile running gpt-5.6-terra via the
     terra proxy, with the new agent's persona as its SOUL);
  2. a FastAPI `/ask` service (net_agents/<name>.py) that brains on that Hermes
     instance with a direct gpt-5.6-terra fallback — identical contract to every
     other agent;
  3. a registry entry in agents.json, so the orchestrator and every other agent
     can immediately call it via the hub (`ask_agent`) — the new agent *is* a
     tool the others can use;
  4. it launches both processes.

This is what makes the network modular and auto-expandable: new capability =
one spec → one new callable agent that appears in the graph.

Contract: POST /ask {"message": "<spec>", "from": "..."} -> {"reply": ...}
Run:  .venv/bin/python -m uvicorn net_agents.builder:app --port 9105
"""
