def build_agent(name: str, purpose: str, persona: str) -> dict:
    slug = _slug(name)
    reg = _registry()
    if slug in reg:
        return {"ok": False, "error": f"agent '{slug}' already exists"}

    brain_port = _free_port(8660, 8699)
    agent_port = _free_port(9110, 9199)
    brain_key = f"{slug}brain-{secrets.token_hex(10)}"  # >=16 chars (Hermes min)

    # 1) Hermes profile (brain)
    subprocess.run([HERMES, "profile", "create", f"{slug}brain",
                    "--description", f"Hermes brain for the {slug} agent"],
                   capture_output=True, text=True, timeout=90)
    pdir = PROFILES / f"{slug}brain"
    (pdir / ".env").write_text(
        f"OPENAI_API_KEY={OPENAI_KEY}\nAPI_SERVER_ENABLED=true\n"
        f"API_SERVER_KEY={brain_key}\nAPI_SERVER_PORT={brain_port}\n")
    os.chmod(pdir / ".env", 0o600)
    (pdir / "config.yaml").write_text(HERMES_CONFIG.format(name=slug, proxy=PROXY_URL))
    (pdir / "SOUL.md").write_text(persona)

    # 2) agent service file
    code = AGENT_TEMPLATE.format(title=name, name=slug, persona=persona,
                                 brain_port=brain_port, brain_key=brain_key)
    (HERE / f"{slug}.py").write_text(code)

    # 2b) persona doc — so clicking the node in the graph shows its docs
    ctx = HERE / "context"
    ctx.mkdir(exist_ok=True)
    (ctx / f"{slug}.md").write_text(
        f"# {name} agent — generated\n\n{purpose}\n\n## Persona\n\n{persona}\n\n"
        f"Brains on its own Hermes instance ({slug}brain) running gpt-5.6-terra "
        f"via the terra proxy. Can delegate to any other agent through the hub "
        f"(list_agents / ask_agent).\n")

    # 3) registry entry — instantly callable by every other agent via the hub
    reg[slug] = {"type": "http", "url": f"http://localhost:{agent_port}/ask",
                 "description": purpose,
                 "brain_port": brain_port, "agent_port": agent_port,
                 "generated": True}
    AGENTS_JSON.write_text(json.dumps(reg, indent=2, ensure_ascii=False))

    # 4) launch brain + service
    logf = STATE / f"{slug}.log"
    subprocess.Popen([HERMES, "-p", f"{slug}brain", "gateway", "run", "--force"],
                     stdout=open(STATE / f"hermes-{slug}brain.log", "a"),
                     stderr=subprocess.STDOUT, cwd=str(ROOT))
    subprocess.Popen([VENV_PY, "-m", "uvicorn", f"net_agents.{slug}:app",
                      "--port", str(agent_port)],
                     stdout=open(logf, "a"), stderr=subprocess.STDOUT, cwd=str(ROOT))

    log.info("built agent '%s' (service :%d, brain :%d)", slug, agent_port, brain_port)
    return {"ok": True, "name": slug, "agent_port": agent_port,
            "brain_port": brain_port,
            "note": "registered + launching; callable as ask_agent('%s')" % slug}


# ── spec parsing (uses the builder's own reasoning via the proxy) ─────────
SPEC_SYSTEM = (
    "You design agents for a modular Hermes agent network. Given a build "
    "request, output ONLY JSON: {\"name\": short-slug, \"purpose\": one-line "
    "description for the registry, \"persona\": a 3-6 sentence system prompt "
    "for the new agent describing its role, how it answers other agents in "
    "short plain text, and when to defer. No markdown.}")


def parse_spec(spec: str) -> dict:
    try:
        r = httpx.post(f"{PROXY_URL}/chat/completions",
                       headers={"Authorization": f"Bearer {OPENAI_KEY}"},
                       json={"model": "gpt-5.6-terra", "reasoning_effort": "none",
                             "max_completion_tokens": 500,
                             "response_format": {"type": "json_object"},
                             "messages": [{"role": "system", "content": SPEC_SYSTEM},
                                          {"role": "user", "content": spec}]},
                       timeout=40)
        r.raise_for_status()
        d = json.loads(r.json()["choices"][0]["message"]["content"])
        return {"name": _slug(d.get("name", "agent")),
                "purpose": d.get("purpose", spec)[:200],
                "persona": d.get("persona", spec)}
    except Exception as e:
        log.warning("spec parse failed: %s", e)
        # fall back to a naive slug from the spec
        m = re.search(r"called ['\"]?([a-zA-Z0-9 ]+)['\"]?", spec)
        nm = _slug(m.group(1)) if m else "agent"
        return {"name": nm, "purpose": spec[:200],
                "persona": f"You are the {nm} agent. {spec}. Answer other "
                           "agents in short plain text."}


class Ask(BaseModel):
    message: str
    from_: str | None = None


@app.post("/ask")
def ask(a: Ask) -> dict:
    spec = a.message.strip()
    try:
        parsed = parse_spec(spec)
        result = build_agent(parsed["name"], parsed["purpose"], parsed["persona"])
        if result.get("ok"):
            return {"reply": f"Built agent '{result['name']}' — it's registered "
                             f"and starting on port {result['agent_port']} with "
                             f"its own Hermes brain (:{result['brain_port']}). "
                             f"Any agent can now call it as ask_agent('{result['name']}')."}
        return {"reply": f"Could not build it: {result.get('error')}"}
    except Exception as e:
        log_failure("builder", "build_failed", str(e), {"spec": spec[:200]})
        return {"reply": f"Build failed: {e}"}


@app.get("/health")
def health() -> dict:
    return {"ok": True, "agent": "builder",
            "generated": [k for k, v in _registry().items()
                          if isinstance(v, dict) and v.get("generated")]}
